console.log("hello world"); //tanpa ada karakter khusus
console.log('\n');

console.log("hello \bworld"); //menggunakan \b 
console.log('\n');

console.log("hello \nworld"); //menggunakan \n
console.log('\n');

console.log("hello \tworld"); //menggunakan \t
console.log('\n');

console.log("hello \'world\'"); //menggunakan \'
console.log('\n');

console.log("hello \"world\""); //menggunakan \" 
console.log('\n');

console.log("hello \\world\\"); //menggunakan \\
console.log('\n');


